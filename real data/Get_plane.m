function [L_p] = Get_plane (Tx)

P1=Tx*[0;0;0;1];P1=P1(1:3,:);
P2=Tx*[1;0;0;1];P2=P2(1:3,:);
P3=Tx*[0;1;0;1];P3=P3(1:3,:);

%%
x1=P1(1,1);
y1=P1(2,1);
z1=P1(3,1);
x2=P2(1,1);
y2=P2(2,1);
z2=P2(3,1);
x3=P3(1,1);
y3=P3(2,1);
z3=P3(3,1);
A=y1*z2-y1*z3-y2*z1+y2*z3+y3*z1-y3*z2;
B=-x1*z2+x1*z3+x2*z1-x2*z3-x3*z1+x3*z2;
C=x1*y2-x1*y3-x2*y1+x2*y3+x3*y1-x3*y2;
D=-x1*y2*z3+x1*y3*z2+x2*y1*z3-x2*y3*z1-x3*y1*z2+x3*y2*z1;
L_p=[A,B,C,D];
